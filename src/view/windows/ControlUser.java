    private void tableUserDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableUserDataMouseClicked

        int selectedRow = tableUserData.rowAtPoint(evt.getPoint());
        numberID.setText(String.valueOf(tableUserData.getValueAt(selectedRow, 0)));
        textName.setText(String.valueOf(tableUserData.getValueAt(selectedRow, 1)));
        comboRank.setSelectedItem(String.valueOf(tableUserData.getValueAt(selectedRow, 2)));
        numberPhone.setText(String.valueOf(tableUserData.getValueAt(selectedRow, 3)));
        textDiscord.setText(String.valueOf(tableUserData.getValueAt(selectedRow, 4)));
        textAnnot.setText(String.valueOf(tableUserData.getValueAt(selectedRow, 5)));
        
        textName.setEnabled(true);
        comboRank.setEnabled(true);
        numberPhone.setEnabled(true);
        textDiscord.setEnabled(true);
        textAnnot.setEnabled(true);
        


    }//GEN-LAST:event_tableUserDataMouseClicked

    private void memberSearchKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_memberSearchKeyTyped

        memberSearch.addKeyListener(new KeyAdapter() {

            @Override
            public void keyReleased(KeyEvent ke) {
                tableRowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + memberSearch.getText(), 1));

            }

        });

        tableRowSorter = new TableRowSorter(tableModel.getDefaultTableModel());
        tableUserData.setRowSorter(tableRowSorter);

    }//GEN-LAST:event_memberSearchKeyTyped

