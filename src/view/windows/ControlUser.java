    private void buttonRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonRegisterActionPerformed
        
        String username = textName.getText();
        String phone = numberPhone.getText();
        String discord = textDiscord.getText();
        int rankGuild = Integer.parseInt((String) comboRank.getSelectedItem());
        String annotations = textAnnot.getText();
        int userID = Integer.parseInt(numberID.getText());

      //Transfers values
        
        userService.validateUpgradeData(userID,username, phone, discord, annotations, rankGuild);

      //Clear data and confirm send
        textName.setText("");
        numberPhone.setText("");
        textDiscord.setText("");
        comboRank.setSelectedItem(9);
        textAnnot.setText("");
        JOptionPane.showMessageDialog(null, "¡Miembro actualizado! \n \n Cambios realizados con éxito");
        
        textName.setEnabled(false);
        comboRank.setEnabled(false);
        numberPhone.setEnabled(false);
        textDiscord.setEnabled(false);
        textAnnot.setEnabled(false);
        numberID.setText("?");


    }//GEN-LAST:event_buttonRegisterActionPerformed

