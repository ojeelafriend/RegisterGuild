    private void buttonRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonRegisterActionPerformed
        String username = textName.getText();
        String phone = numberPhone.getText();
        String discord = textDiscord.getText();
        int rankGuild = Integer.parseInt((String) comboRank.getSelectedItem());
        String annotations = textAnnot.getText();

      //Transfers values
        userService.validateCreationUser(username, phone, discord, annotations, rankGuild);

        //Clear data and confirm send
        textName.setText("");
        numberPhone.setText("");
        textDiscord.setText("");
        comboRank.setSelectedItem(9);
        textAnnot.setText("");
        JOptionPane.showMessageDialog(null, "¬°Nuevo miembro registrado con √©xito! \n \n Para m√°s informaci√≥n consulte el control de miembros");

    }//GEN-LAST:event_buttonRegisterActionPerformed

    private void numberPhoneKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_numberPhoneKeyTyped
        char c = evt.getKeyChar();

        if (c < '0' || c > '9') {
            evt.consume();
        }
    }//GEN-LAST:event_numberPhoneKeyTyped

